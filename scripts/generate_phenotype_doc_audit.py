"""Generate docs/PHENOTYPE-DOC-AUDIT.md — per-phenotype documentation state.

For each Synthea module under synthea/modules/custom/phekb_*.json, classify
its PheKB-doc state as:
  A — Full analysis (data/phekb-raw/<dir>/document_analysis.json present)
  B — Algorithm doc (.doc/.docx/.pdf) present, analysis missing
  C — Only metadata stub (description.txt + details.json), no algorithm doc
  D — No data/phekb-raw/ dir at all

Output: docs/PHENOTYPE-DOC-AUDIT.md with per-phenotype state, source file(s),
and code-augmentation status (entries in data/code_augmentations.json).
"""
from __future__ import annotations
import json
import re
import sys
from pathlib import Path
from collections import defaultdict

REPO = Path(__file__).resolve().parent.parent
RAW = REPO / "data" / "phekb-raw"
MODULES = REPO / "synthea" / "modules" / "custom"
AUG_MAP = REPO / "data" / "code_augmentations.json"
OUT = REPO / "docs" / "PHENOTYPE-DOC-AUDIT.md"

sys.path.insert(0, str(REPO / "scripts"))
from audit_phenotypes_vs_phekb import EXPLICIT_ALIASES  # noqa: E402


def find_phekb_dir(slug: str) -> Path | None:
    """Find the phekb-raw subdir for a given module slug, using aliases + fuzzy matching."""
    if slug in EXPLICIT_ALIASES:
        target = EXPLICIT_ALIASES[slug]
        if target is None:
            return None
        d = RAW / target
        return d if d.is_dir() else None
    if (RAW / slug).is_dir():
        return RAW / slug
    # Token overlap fallback
    slug_tokens = set(re.findall(r"[a-z0-9]+", slug))
    if not slug_tokens:
        return None
    for d in sorted(RAW.iterdir()):
        if not d.is_dir():
            continue
        d_tokens = set(re.findall(r"[a-z0-9]+", d.name))
        if slug_tokens.issubset(d_tokens):
            return d
    return None


def classify(d: Path | None) -> tuple[str, list[str]]:
    if d is None:
        return "D", []
    if (d / "document_analysis.json").exists():
        return "A", []
    docs = sorted(f.name for f in d.iterdir() if f.suffix.lower() in (".doc", ".docx", ".pdf"))
    if docs:
        return "B", docs
    stub = sorted(f.name for f in d.iterdir())
    return "C", stub


def module_snomeds(mod_path: Path) -> list[str]:
    text = mod_path.read_text(encoding="utf-8")
    return sorted(set(re.findall(r'"system":\s*"SNOMED-CT"[^}]*?"code":\s*"(\d+)"', text)))


def main() -> int:
    aug = json.loads(AUG_MAP.read_text(encoding="utf-8")) if AUG_MAP.exists() else {}

    rows = []
    for mod in sorted(MODULES.glob("phekb_*.json")):
        if "_control" in mod.stem:
            continue
        slug = mod.stem.replace("phekb_", "").replace("_", "-")
        d = find_phekb_dir(slug)
        state, files = classify(d)
        snomeds = module_snomeds(mod)
        n_aug = sum(1 for s in snomeds if s in aug)
        rows.append({
            "slug": slug,
            "state": state,
            "phekb_dir": d.name if d else "",
            "files": files,
            "snomeds": len(snomeds),
            "snomeds_with_aug": n_aug,
        })

    counts = defaultdict(int)
    for r in rows:
        counts[r["state"]] += 1

    with OUT.open("w", encoding="utf-8") as f:
        f.write("# Phenotype Documentation State Audit\n\n")
        f.write("Generated by `scripts/generate_phenotype_doc_audit.py`. ")
        f.write(f"Total phenotypes (Synthea modules): {len(rows)}.\n\n")
        f.write("## State definitions\n\n")
        f.write("- **A** — Full PheKB analysis available (`document_analysis.json` present)\n")
        f.write("- **B** — Algorithm document (`.doc`/`.docx`/`.pdf`) present but analysis missing — re-extractable via `scripts/extract_phekb_doc_analysis.py`\n")
        f.write("- **C** — Only PheKB metadata stub (`description.txt` + `details.json`); algorithm doc never downloaded\n")
        f.write("- **D** — No `data/phekb-raw/` dir at all — phenotype not scraped from PheKB (built clinical-knowledge-only)\n\n")
        f.write("## Counts\n\n")
        for s in "ABCD":
            f.write(f"- **State {s}**: {counts[s]}\n")
        f.write("\n")

        f.write("## Per-phenotype detail\n\n")
        f.write("| # | Phenotype | State | PheKB dir | Source files | SNOMEDs in module | with augmentation |\n")
        f.write("|---|---|---|---|---|---|---|\n")
        for i, r in enumerate(rows, 1):
            files_str = ", ".join(r["files"][:3])
            if len(r["files"]) > 3:
                files_str += f", +{len(r['files'])-3} more"
            f.write(
                f"| {i} | {r['slug']} | {r['state']} | "
                f"{r['phekb_dir']} | {files_str} | "
                f"{r['snomeds']} | {r['snomeds_with_aug']} |\n"
            )

        f.write("\n## State-B detail (re-extractable)\n\n")
        for r in rows:
            if r["state"] != "B":
                continue
            f.write(f"### {r['slug']}\n\n")
            f.write(f"- PheKB dir: `data/phekb-raw/{r['phekb_dir']}/`\n")
            for fn in r["files"]:
                f.write(f"- Source: `{fn}`\n")
            f.write("- Re-extract with: `python scripts/extract_phekb_doc_analysis.py --dir " + r["phekb_dir"] + "`\n\n")

        f.write("## State-C detail (PheKB stub only)\n\n")
        for r in rows:
            if r["state"] != "C":
                continue
            f.write(f"- `{r['slug']}` → `data/phekb-raw/{r['phekb_dir']}/` (files: {', '.join(r['files'][:5])})\n")

        f.write("\n## State-D detail (no PheKB dir)\n\n")
        for r in rows:
            if r["state"] != "D":
                continue
            f.write(f"- `{r['slug']}` — clinical-knowledge-only phenotype\n")

    print(f"Wrote {OUT}")
    print(f"State counts: {dict(counts)}")
    return 0


if __name__ == "__main__":
    sys.stdout.reconfigure(encoding="utf-8")
    sys.exit(main())
