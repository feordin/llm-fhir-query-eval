"""Build a 75/25 evaluation workload split across two machines.

Counts test cases per phenotype (matching against Synthea modules), then bin-packs
phenotypes into two work streams with target proportions:
  - main_pc: 75% of total cells (3 prompts × 3 tiers × N test cases per phenotype)
  - snapdragon: 25% of total cells

Output: docs/EVAL-WORKLOADS.md — checked into git, both machines read it.

Algorithm: sort phenotypes by test-case count descending, then greedy-assign each
to whichever bin is currently below its target ratio. Produces a roughly balanced
split where the slower machine gets fewer cells.
"""
from __future__ import annotations
import json
from collections import defaultdict
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
TC_DIR = REPO / "test-cases" / "phekb"
MOD_DIR = REPO / "synthea" / "modules" / "custom"
OUT = REPO / "docs" / "EVAL-WORKLOADS.md"
OUT_JSON = REPO / "docs" / "eval-workloads.json"

MAIN_PC_TARGET = 0.75  # 75% to faster machine
PROMPT_VARIANTS = 3
TIERS = 3
CELLS_PER_TC = PROMPT_VARIANTS * TIERS  # 9


def load_canonical_phenotypes() -> set[str]:
    """The 108 canonical phenotypes are those with Synthea modules."""
    return {
        p.stem.replace("phekb_", "").replace("_", "-")
        for p in MOD_DIR.glob("phekb_*.json")
        if "_control" not in p.stem
    }


def count_test_cases_per_phenotype(canonical: set[str]) -> dict[str, list[str]]:
    """Map canonical phenotype slug -> list of test-case file IDs targeting it.

    Test cases reference phenotypes via `source_id`. We bucket by canonical slug
    if the source_id matches; otherwise we try fuzzy matching against canonical
    slugs (fallback for legacy long slugs).
    """
    by_phen: dict[str, list[str]] = defaultdict(list)
    for f in sorted(TC_DIR.glob("phekb-*.json")):
        with f.open(encoding="utf-8") as fh:
            data = json.load(fh)
        sid = data.get("source_id", "")
        # Direct match
        if sid in canonical:
            by_phen[sid].append(f.stem)
            continue
        # Try fuzzy: any canonical slug substring of source_id?
        matched = None
        for c in canonical:
            if c in sid or sid in c:
                matched = c
                break
        if matched:
            by_phen[matched].append(f.stem)
        else:
            # Phenotype not in canonical list — skip (likely a legacy/duplicate slug)
            pass
    return by_phen


def split_workload(by_phen: dict[str, list[str]]) -> tuple[dict, dict]:
    """Greedy 75/25 bin-pack by total cell count."""
    items = sorted(by_phen.items(), key=lambda kv: -len(kv[1]))
    total_cells = sum(len(v) for v in by_phen.values()) * CELLS_PER_TC
    main_target = total_cells * MAIN_PC_TARGET
    snap_target = total_cells * (1 - MAIN_PC_TARGET)

    main_pc: dict[str, list[str]] = {}
    snapdragon: dict[str, list[str]] = {}
    main_cells = 0
    snap_cells = 0

    for phen, tcs in items:
        cells = len(tcs) * CELLS_PER_TC
        # Assign to whichever is further from its target
        main_deficit = main_target - main_cells
        snap_deficit = snap_target - snap_cells
        if main_deficit / max(main_target, 1) >= snap_deficit / max(snap_target, 1):
            main_pc[phen] = tcs
            main_cells += cells
        else:
            snapdragon[phen] = tcs
            snap_cells += cells

    return main_pc, snapdragon


def main() -> int:
    canonical = load_canonical_phenotypes()
    print(f"Canonical phenotypes (Synthea modules): {len(canonical)}")
    by_phen = count_test_cases_per_phenotype(canonical)
    print(f"Phenotypes with at least one test case: {len(by_phen)}")
    total_tcs = sum(len(v) for v in by_phen.values())
    total_cells = total_tcs * CELLS_PER_TC
    print(f"Total test cases: {total_tcs} | Total cells (×9): {total_cells}")

    main_pc, snapdragon = split_workload(by_phen)
    main_tcs = sum(len(v) for v in main_pc.values())
    snap_tcs = sum(len(v) for v in snapdragon.values())
    print(f"\nmain_pc: {len(main_pc):3d} phenotypes, {main_tcs:3d} test cases, "
          f"{main_tcs * CELLS_PER_TC} cells ({main_tcs / total_tcs:.0%})")
    print(f"snapdragon: {len(snapdragon):3d} phenotypes, {snap_tcs:3d} test cases, "
          f"{snap_tcs * CELLS_PER_TC} cells ({snap_tcs / total_tcs:.0%})")

    # Write the workload doc
    with OUT.open("w", encoding="utf-8") as f:
        f.write("# Evaluation Workload Split\n\n")
        f.write(f"Generated by `scripts/build_eval_workload.py`. "
                f"Machines read this file via `scripts/run_workload.py --machine <name>`.\n\n")
        f.write("Each cell = 1 test case × 1 prompt variant × 1 tier. Each phenotype "
                f"contributes (# test cases × {CELLS_PER_TC}) cells. Split is roughly "
                f"{MAIN_PC_TARGET:.0%} / {1 - MAIN_PC_TARGET:.0%} by cell count.\n\n")
        f.write(f"## Summary\n\n")
        f.write(f"| Machine | Phenotypes | Test cases | Cells |\n")
        f.write(f"|---|---|---|---|\n")
        f.write(f"| `main_pc` | {len(main_pc)} | {main_tcs} | {main_tcs * CELLS_PER_TC} ({main_tcs / total_tcs:.0%}) |\n")
        f.write(f"| `snapdragon` | {len(snapdragon)} | {snap_tcs} | {snap_tcs * CELLS_PER_TC} ({snap_tcs / total_tcs:.0%}) |\n")
        f.write(f"| **Total** | **{len(by_phen)}** | **{total_tcs}** | **{total_cells}** |\n\n")
        f.write("## main_pc — fast workstation (75%)\n\n")
        f.write("```\n")
        for phen, tcs in sorted(main_pc.items()):
            f.write(f"{phen}\n")
        f.write("```\n\n")
        f.write("### main_pc test-case detail\n\n")
        f.write("| Phenotype | Test cases |\n|---|---|\n")
        for phen, tcs in sorted(main_pc.items()):
            f.write(f"| `{phen}` | {len(tcs)} |\n")
        f.write("\n## snapdragon — Surface 7 Pro (25%)\n\n")
        f.write("```\n")
        for phen, tcs in sorted(snapdragon.items()):
            f.write(f"{phen}\n")
        f.write("```\n\n")
        f.write("### snapdragon test-case detail\n\n")
        f.write("| Phenotype | Test cases |\n|---|---|\n")
        for phen, tcs in sorted(snapdragon.items()):
            f.write(f"| `{phen}` | {len(tcs)} |\n")
    print(f"\nWrote {OUT}")

    # JSON sidecar — machine-readable for run_workload.py
    json_data = {
        "generated_by": "scripts/build_eval_workload.py",
        "main_pc_target_pct": MAIN_PC_TARGET,
        "cells_per_test_case": CELLS_PER_TC,
        "workloads": {
            "main_pc": {
                "phenotypes": sorted(main_pc.keys()),
                "test_cases_by_phenotype": {k: sorted(v) for k, v in main_pc.items()},
                "test_case_count": main_tcs,
                "cell_count": main_tcs * CELLS_PER_TC,
            },
            "snapdragon": {
                "phenotypes": sorted(snapdragon.keys()),
                "test_cases_by_phenotype": {k: sorted(v) for k, v in snapdragon.items()},
                "test_case_count": snap_tcs,
                "cell_count": snap_tcs * CELLS_PER_TC,
            },
        },
    }
    with OUT_JSON.open("w", encoding="utf-8") as f:
        json.dump(json_data, f, indent=2)
    print(f"Wrote {OUT_JSON}")
    return 0


if __name__ == "__main__":
    main()
